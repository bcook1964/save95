$(".collapsible").click(function () {
  $(this).find('#arrow').toggleClass('fas fa-caret-right');
  if ($("button").not(this).find("#arrow").hasClass("fas fa-caret-right")) {
      $("button").not(this).find("#arrow").removeClass('fas fa-caret-right');
      $("button").not(this).find("#arrow").addClass('fa-solid fa-caret-down');
  }
  else {
      $("button").not(this).find("#arrow").removeClass('fa-solid fa-caret-down');
      $("button").not(this).find("#arrow").addClass('fas fa-caret-right');
  }
});

